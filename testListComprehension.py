import numpy as np

a = [i for i in range(10)]
print()
print('Results for list datatype')
print('a : ', a)
print()
print('2*a : ', 2*a)
print()
print('a + a : ', a + a)
print()
print()

b = np.array(a)
print()
print('Result for array datatype')
print('b : ', b)
print()
print('2*b : ', 2*b)
print()
print('b + b : ', b + b)
print()
